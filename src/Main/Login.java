    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
        String username = labelUsername.getText();
        String password = String.valueOf(labelPassword.getPassword());
        if(username.isEmpty() &&  password.isEmpty()){
            JOptionPane.showMessageDialog(this,"Username / Password Kosong","PERINGATAN!",JOptionPane.ERROR_MESSAGE);
        }else if(username.isEmpty()){
            JOptionPane.showMessageDialog(this, " Username belum diisi", "PERINGATAN!", JOptionPane.WARNING_MESSAGE);
        }else if(password.isEmpty()){
            JOptionPane.showMessageDialog(this," Password Kosong","PERINGATAN!",JOptionPane.WARNING_MESSAGE);
        }
        else{
            try {          
                userLogin(username,password);
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_LoginActionPerformed

