    private void userLogin(String username, String password) throws SQLException {
        Connection dbconn = Koneksi.getKoneksi();
        if(dbconn != null){
        try{
            PreparedStatement st = (PreparedStatement)
                dbconn.prepareStatement("Select * from login WHERE username = ? AND password = ?");
                
            
            st.setString(1, username);
            st.setString(2, password);
            ResultSet res = st.executeQuery();
                if(res.next()){
                    dispose();
                    DashboardAdmin d = new DashboardAdmin();
                    d.setTitle("Dashboard");
                    d.setVisible(true);
                }else{
                  System.out.println("username " + username);
                  System.out.println("password " + password);
                  JOptionPane.showMessageDialog(this,"Username / Password Tidak Ditemukan","Error",JOptionPane.ERROR_MESSAGE); 
                }
            
        }catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            System.out.println("Koneksi tidak tersedia.");
        }
    }
